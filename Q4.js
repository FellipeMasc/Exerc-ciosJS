function nomeDoMes(numero){
    switch(numero){
        case 1: console.log(`O mês é janeiro`)
                break
        case 2: console.log(`O mês é fevereiro`)
                break
        case 3: console.log(`O mês é março`)
                break
        case 4: console.log(`O mês é abril`)
                break
        case 5: console.log(`O mês é maio`)
                break
        case 6: console.log(`O mês é junho`)
                break
        case 7: console.log(`O mês é julho`)
                break
        case 8: console.log(`O mês é agosto`)
                break
        case 9: console.log(`O mês é setembro`)
                break
        case 10: console.log(`O mês é outubro`)
                break
        case 11: console.log(`O mês é novembro`)
                break
        case 12: console.log(`O mês é dezembro`)
                break
        default: console.log(`Mês não válido`)
    }
}
nomeDoMes(11)
nomeDoMes(13)